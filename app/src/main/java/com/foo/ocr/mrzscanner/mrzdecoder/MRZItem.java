package com.foo.ocr.mrzscanner.mrzdecoder;

public class MRZItem {

    String name;
    String data;

    public MRZItem(String name, String data) {
        this.name = name;
        this.data = data;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }
}
